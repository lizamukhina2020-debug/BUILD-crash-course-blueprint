const generateChatId = (): string => {
  return `chat_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
};

// Generic conversation starters that shouldn't be used as unique titles
const GENERIC_STARTERS = [
  "i'm struggling with money",
  "my relationships feel stuck",
  "i can't find inner peace",
  "i feel stuck in my career",
  "i'm dealing with health issues",
  "i feel lonely and isolated",
];

// Generic phrases to remove from titles
const GENERIC_PHRASES = [
  "i'm struggling with",
  "i struggle with",
  "i'm stressed about",
  "i'm worried about",
  "i'm having trouble with",
  "i need help with",
  "i'm concerned about",
  "i'm anxious about",
  "i'm afraid of",
  "i'm scared of",
  "i want to",
  "i need to",
  "i'm feeling",
  "i feel like",
  "i feel",
  "i have been",
  "i have",
  "i am",
  "i'm",
];

// Check if a message is a generic conversation starter
const isGenericStarter = (message: string): boolean => {
  const lowerMessage = message.toLowerCase().trim();
  return GENERIC_STARTERS.some(starter => 
    lowerMessage === starter || lowerMessage.startsWith(starter)
  );
};

// Clean a message by removing generic phrases
const cleanMessageForTitle = (message: string): string => {
  let cleanMessage = message.toLowerCase().trim();
  
  for (const phrase of GENERIC_PHRASES) {
    if (cleanMessage.startsWith(phrase)) {
      cleanMessage = cleanMessage.substring(phrase.length).trim();
      break;
    }
  }
  
  return cleanMessage;
};

// Find the most unique/specific message from a list of user messages
const findMostUniqueMessage = (userMessages: string[]): string => {
  // Filter out generic starters and very short messages
  const specificMessages = userMessages.filter(msg => {
    const lower = msg.toLowerCase().trim();
    // Skip generic starters
    if (isGenericStarter(msg)) return false;
    // Skip very short responses like "yes", "no", "okay"
    if (lower.length < 15) return false;
    // Skip common short responses
    if (['yes', 'no', 'okay', 'ok', 'sure', 'thanks', 'thank you'].includes(lower)) return false;
    return true;
  });
  
  if (specificMessages.length > 0) {
    // Return the first specific message (usually contains the problem details)
    return specificMessages[0];
  }
  
  // Fallback to first message if no specific ones found
  return userMessages[0] || '';
};

const generateTitle = (message: string, category: string): string => {
  const cleanMessage = cleanMessageForTitle(message);
  
  // Capitalize first letter and get first 40 chars of the unique part
  const uniquePart = cleanMessage.charAt(0).toUpperCase() + cleanMessage.slice(1);
  const preview = uniquePart.length > 40 
    ? uniquePart.substring(0, 40) + '...'
    : uniquePart;
  
  // Category emoji map
  const categoryEmojis: Record<string, string> = {
    money: '💰',
    loneliness: '💝',
    relationship: '❤️',
    career: '⭐',
    health: '🌿',
    general: '🎯',
    peace: '🧘',
  };
  
  const emoji = categoryEmojis[category] || '✨';
  return `${emoji} ${preview}`;
};

// Generate a smart title from all user messages in a conversation
const generateSmartTitle = (userMessages: string[], category: string): string => {
  const bestMessage = findMostUniqueMessage(userMessages);
  return generateTitle(bestMessage, category);
};

