export interface ChatMessage {
  id: string;
  text: string;
  isUser: boolean;
  timestamp: string;
}

export interface LoggedSeedBasic {
  id: string;
  action: string;
}

export type ChatPhase = 1 | 2 | 3 | 4 | 5 | 6 | 7 | 'completed';

// Personalized seed suggestion (matches deepseekApi.ts PersonalizedSeed)
export interface StoredPersonalizedSeed {
  emoji: string;
  action: string;
  theyFeel: string;
  youReceive: string;
}

// Personalized content generated by DeepSeek for this conversation
export interface StoredPersonalizedContent {
  mirrorExplanation: string;
  reciprocalLaw: string;
  seeds: StoredPersonalizedSeed[];
}

export interface ChatConversation {
  id: string;
  title: string;
  category: string;
  // Optional custom label for the "general/Another goal" category (Garden picker + subtitle).
  // This does NOT rename the journey title/problem title; it's only the category label.
  generalCategoryLabel?: string;
  // Optional journey customization when category is `general` ("Other goal")
  journeyDisplayEmoji?: string;
  journeyMeditationId?: string;
  // Language of this conversation thread (so history stays in the original language)
  language?: 'en' | 'ru';
  messages: ChatMessage[];
  phase: ChatPhase;
  seedsLogged: LoggedSeedBasic[];
  meditationCompleted: boolean;
  createdAt: string;
  updatedAt: string;
  // Personalized content (generated by DeepSeek, persisted for consistency)
  personalizedContent?: StoredPersonalizedContent;
  // Whether this was the user's first conversation (for layout decisions)
  isFirstConversation?: boolean;
  // Experience selection data (for personalization)
  selectedExperiences?: string[];
  experienceOptions?: { emoji: string; text: string }[];
  // Heavy topic flag (for different flow: war, disability, deep trauma)
  isHeavyTopic?: boolean;
  // Post-completion mode flags (for restoring chat state)
  isInPostCompletionMode?: boolean;
  postCompletionChatMode?: boolean;
  // Conversation mode: 'problem' for challenges, 'goal' for aspirations
  conversationMode?: 'problem' | 'goal';

  // Phase 1 monetization: when true, this conversation is allowed to plant seeds (consumes a "Garden Ticket" once)
  gardenTicketed?: boolean;
}

