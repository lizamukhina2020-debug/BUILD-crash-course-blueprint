// Update conversation category and title (uses smart title from all user messages)
export const updateConversationCategory = async (
  chatId: string,
  category: string,
  _firstMessage: string // Kept for backward compatibility, but we now look at all messages
): Promise<boolean> => {
  try {
    // Get the conversation to access all messages
    const conversation = await getConversation(chatId);
    if (!conversation) return false;
    
    // Get all user messages
    const userMessages = conversation.messages
      .filter(m => m.isUser)
      .map(m => m.text);
    
    // Generate smart title from the most unique message
    const smartTitle = userMessages.length > 0 
      ? generateSmartTitle(userMessages, category)
      : generateTitle(_firstMessage, category);
    
    const result = await updateConversation(chatId, { 
      category,
      title: smartTitle,
    });
    return result !== null;
  } catch (error) {
    console.error('Error updating category:', error);
    return false;
  }
};

// Refresh conversation title based on current messages (call when more context is available)
export const refreshConversationTitle = async (chatId: string): Promise<boolean> => {
  try {
    const conversation = await getConversation(chatId);
    if (!conversation) return false;
    
    // Get all user messages
    const userMessages = conversation.messages
      .filter(m => m.isUser)
      .map(m => m.text);
    
    if (userMessages.length === 0) return false;
    
    // Generate smart title from the most unique message
    const smartTitle = generateSmartTitle(userMessages, conversation.category);
    
    // Only update if the title would actually change
    if (smartTitle !== conversation.title) {
      const result = await updateConversation(chatId, { title: smartTitle });
      return result !== null;
    }
    
    return true;
  } catch (error) {
    console.error('Error refreshing conversation title:', error);
    return false;
  }
};

