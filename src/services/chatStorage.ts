// Format conversation for display
export const formatConversationDate = (dateString: string): string => {
  const date = new Date(dateString);
  const now = new Date();
  const diffDays = Math.floor((now.getTime() - date.getTime()) / (1000 * 60 * 60 * 24));
  
  if (diffDays === 0) {
    return 'Today';
  } else if (diffDays === 1) {
    return 'Yesterday';
  } else if (diffDays < 7) {
    return `${diffDays} days ago`;
  } else {
    return date.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
  }
};

// Get phase display text
export const getPhaseDisplayText = (phase: ChatPhase): string => {
  if (phase === 'completed') return 'Completed ✓';
  
  const phaseTexts: Record<number, string> = {
    1: 'Sharing feelings',
    2: 'Exploring the mirror',
    3: 'Understanding seeds',
    4: 'Getting ideas',
    5: 'Ready to plant',
    6: 'Logging seeds',
    7: 'Meditating',
  };
  
  return phaseTexts[phase as number] || 'In progress';
};


