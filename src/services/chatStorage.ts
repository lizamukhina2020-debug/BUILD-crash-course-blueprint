// Get all conversations
export const getAllConversations = async (): Promise<ChatConversation[]> => {
  try {
    const data = await AsyncStorage.getItem(CHAT_KEYS.CONVERSATIONS);
    if (data) {
      const conversations: ChatConversation[] = JSON.parse(data);
      // Sort by updatedAt, most recent first
      return conversations.sort((a, b) => 
        new Date(b.updatedAt).getTime() - new Date(a.updatedAt).getTime()
      );
    }
    return [];
  } catch (error) {
    console.error('Error getting conversations:', error);
    return [];
  }
};

// Get a single conversation by ID
export const getConversation = async (chatId: string): Promise<ChatConversation | null> => {
  try {
    const conversations = await getAllConversations();
    return conversations.find(c => c.id === chatId) || null;
  } catch (error) {
    console.error('Error getting conversation:', error);
    return null;
  }
};

export const markConversationGardenTicketed = async (chatId: string): Promise<void> => {
  try {
    const conversations = await getAllConversations();
    const idx = conversations.findIndex(c => c.id === chatId);
    if (idx === -1) return;
    const now = new Date().toISOString();
    const updated: ChatConversation = {
      ...conversations[idx],
      gardenTicketed: true,
      updatedAt: now,
    };
    const next = [...conversations];
    next[idx] = updated;
    await AsyncStorage.setItem(CHAT_KEYS.CONVERSATIONS, JSON.stringify(next));
    scheduleSnapshotSync();
  } catch (error) {
    console.error('Error marking conversation gardenTicketed:', error);
  }
};

// Get the active chat ID
export const getActiveChatId = async (): Promise<string | null> => {
  try {
    return await AsyncStorage.getItem(CHAT_KEYS.ACTIVE_CHAT_ID);
  } catch (error) {
    console.error('Error getting active chat ID:', error);
    return null;
  }
};

// Set the active chat ID
export const setActiveChatId = async (chatId: string | null): Promise<void> => {
  try {
    if (chatId) {
      await AsyncStorage.setItem(CHAT_KEYS.ACTIVE_CHAT_ID, chatId);
    } else {
      await AsyncStorage.removeItem(CHAT_KEYS.ACTIVE_CHAT_ID);
    }
  } catch (error) {
    console.error('Error setting active chat ID:', error);
  }
};

export const setForceOpenChatId = async (chatId: string): Promise<void> => {
  try {
    const trimmed = String(chatId || '').trim();
    if (!trimmed) return;
    await AsyncStorage.setItem(FORCE_OPEN_CHAT_ID_KEY, trimmed);
  } catch {
    // ignore
  }
};

export const consumeForceOpenChatId = async (): Promise<string | null> => {
  try {
    const raw = await AsyncStorage.getItem(FORCE_OPEN_CHAT_ID_KEY);
    if (!raw) return null;
    await AsyncStorage.removeItem(FORCE_OPEN_CHAT_ID_KEY);
    return String(raw || '').trim() || null;
  } catch {
    return null;
  }
};

// Create a new conversation
export const createConversation = async (
  firstUserMessage: string,
  category: string = 'general',
  language: 'en' | 'ru' = 'en'
): Promise<ChatConversation> => {
  try {
    const conversations = await getAllConversations();
    
    // Check if this is the user's first conversation (no completed conversations yet)
    const hasCompletedConversation = conversations.some(c => 
      c.phase === 'completed' || c.meditationCompleted
    );
    
    const newConversation: ChatConversation = {
      id: generateChatId(),
      title: generateTitle(firstUserMessage, category),
      category,
      language,
      messages: [],
      phase: 1,
      seedsLogged: [],
      meditationCompleted: false,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
      isFirstConversation: !hasCompletedConversation,
    };
    
    conversations.unshift(newConversation);
    await AsyncStorage.setItem(CHAT_KEYS.CONVERSATIONS, JSON.stringify(conversations));
    await setActiveChatId(newConversation.id);
    scheduleSnapshotSync('chat:createConversation');
    trackEvent('journey_created', {
      journey_id: newConversation.id,
      category,
      language,
      first_message_len: bucketTextLength(firstUserMessage),
    }).catch(() => {});
    
    return newConversation;
  } catch (error) {
    console.error('Error creating conversation:', error);
    throw error;
  }
};

// Update a conversation
export const updateConversation = async (
  chatId: string,
  updates: Partial<Omit<ChatConversation, 'id' | 'createdAt'>>
): Promise<ChatConversation | null> => {
  try {
    const conversations = await getAllConversations();
    const index = conversations.findIndex(c => c.id === chatId);
    
    if (index === -1) return null;
    
    conversations[index] = {
      ...conversations[index],
      ...updates,
      updatedAt: new Date().toISOString(),
    };
    
    await AsyncStorage.setItem(CHAT_KEYS.CONVERSATIONS, JSON.stringify(conversations));
    scheduleSnapshotSync('chat:updateConversation');
    return conversations[index];
  } catch (error) {
    console.error('Error updating conversation:', error);
    return null;
  }
};

// Add messages to a conversation
export const addMessagesToConversation = async (
  chatId: string,
  newMessages: ChatMessage[]
): Promise<boolean> => {
  try {
    const conversations = await getAllConversations();
    const index = conversations.findIndex(c => c.id === chatId);
    
    if (index === -1) return false;
    
    conversations[index].messages = [
      ...conversations[index].messages,
      ...newMessages,
    ];
    conversations[index].updatedAt = new Date().toISOString();
    
    await AsyncStorage.setItem(CHAT_KEYS.CONVERSATIONS, JSON.stringify(conversations));
    scheduleSnapshotSync('chat:addMessages');
    return true;
  } catch (error) {
    console.error('Error adding messages:', error);
    return false;
  }
};

